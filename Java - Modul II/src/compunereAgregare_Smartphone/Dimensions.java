package compunereAgregare_Smartphone;

public class Dimensions {
	
	private final int length;
	private final int width;
	private final int depth;
	
	public int getLength() {
		return length;
	}

	public int getWidth() {
		return width;
	}

	public int getDepth() {
		return depth;
	}

	public Dimensions(int length, int width, int depth) {
		this.length = length;
		this.width = width;
		this.depth = depth;
	}
	
}
